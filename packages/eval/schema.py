from __future__ import annotations

from datetime import datetime, timezone
from typing import Literal

from pydantic import BaseModel, Field


Intent = Literal["code_search", "skill_discovery", "doc_lookup", "memory_recall", "general"]


class GoldenItem(BaseModel):
    id: str
    query: str
    intent: Intent = "general"
    expected_collections: list[str] = Field(default_factory=list)
    expected_files: list[str] = Field(default_factory=list)
    expected_chunks: list[str] = Field(default_factory=list)
    relevance_grades: dict[str, int] = Field(default_factory=dict)
    k_relevant: int = 1
    notes: str = ""


class GoldenCandidate(BaseModel):
    """A candidate generated by the LLM, awaiting human review."""

    id: str
    query: str
    intent: Intent
    suggested_files: list[str] = Field(default_factory=list)
    source_collection: str
    source_chunk_preview: str = ""
    reviewed: bool = False
    accepted: bool = False


class QueryResult(BaseModel):
    """Result of a single query against the running Mnemos system."""

    item_id: str
    retrieved_files: list[str]
    retrieved_collections: list[str]
    retrieved_scores: list[float]
    latency_ms: float
    raw_top_k: list[dict] = Field(default_factory=list)


class IntentMetrics(BaseModel):
    intent: Intent
    n_questions: int
    mrr: float
    ndcg_at_5: float
    recall_at_1: float
    recall_at_3: float
    recall_at_5: float
    recall_at_10: float
    precision_at_5: float
    hit_rate_at_5: float


class MetricsReport(BaseModel):
    tag: str
    created_at: str = Field(default_factory=lambda: datetime.now(timezone.utc).isoformat())
    n_questions: int
    overall: IntentMetrics
    by_intent: list[IntentMetrics] = Field(default_factory=list)
    latency_p50_ms: float = 0.0
    latency_p95_ms: float = 0.0


class EvalRun(BaseModel):
    tag: str
    created_at: str = Field(default_factory=lambda: datetime.now(timezone.utc).isoformat())
    mnemos_url: str
    golden_path: str
    results: list[QueryResult] = Field(default_factory=list)
    report: MetricsReport | None = None
